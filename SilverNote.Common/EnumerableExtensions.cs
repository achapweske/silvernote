using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace SilverNote.Common
{
    public static class EnumerableExtensions
    {
        public static void ForEach<T>(this IEnumerable<T> items, Action<T> action)
        {
            foreach (T item in items)
            {
                action(item);
            }
        }
    }
}
